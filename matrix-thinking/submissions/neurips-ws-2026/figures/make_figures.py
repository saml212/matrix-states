"""Regenerate every figure in this paper directly from archived experiment
JSONs -- no hand-typed numbers. Each figure function documents exactly which
archived file(s) it reads and which paper table/claim (if any) it visualizes.

Run from anywhere:
    python3 figures/make_figures.py
    (or)  python3 matrix-thinking/submissions/neurips-ws-2026/figures/make_figures.py

Requires: numpy, matplotlib (pure-Python analysis, no torch/GPU needed).
Outputs vector PDFs into this same `figures/` directory.

Data provenance (traced by an independent research pass before this script
was written; see the commit that added this file):
  - fig_taskd_forcerank.pdf   <- matrix-thinking/chapter2/results/overnight_snapshots/AGGREGATE_latest.json
                                 (the 991-run Task D snapshot behind Table 2, tab:taskd-m3)
  - fig_taske_depth.pdf       <- experiment-runs/2026-07-02_task_e_zdump/task_e_40k_zdump/*.json
                                 (Table 3, tab:taske-depth; predicted curve regenerated by
                                 literally importing and calling chapter2/analyze_zdump.py,
                                 not re-derived by hand)
  - fig_frontier_exactness.pdf <- experiment-runs/2026-07-02_stage0_waves/{probe_100k,probe_d48_100k,probe_d64_150k}/*.json
                                 (Table 5, tab:frontier-d4)
  - fig_eval_trunc_staircase.pdf <- experiment-runs/2026-07-02_deltanet_waves/eval_trunc/*.json
                                 (supplementary/illustrative -- see caption; NOT behind any
                                 table currently in the main text, see figures/README note
                                 in the script docstring below)

IMPORTANT SCOPING NOTE: experiment-runs/2026-07-03_deltanet_rd_waves/ (deephop,
waveA, exactness/wavegeo3) is a newer, still-running campaign reserved for a
separate full paper (per matrix-thinking/DELTANET_REALDATA_DESIGN.md and
DELTANET_RD_EXACTNESS_DESIGN.md, both dated 2026-07-03, chronologically after
everything this paper reports). This script deliberately does not read from
that directory. Do not add it here without re-checking the paper's stated
scope in sections/01_intro.tex and sections/07_discussion.tex first.
"""
